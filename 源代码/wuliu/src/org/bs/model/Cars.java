package org.bs.model;

public class Cars {
	private int id;
	private String name;
	private String leixing;
	private String zaizhong;
	private String xiulicontent;
	private String jiayou;
	private String beizhucontent;
	private String jiashiyuancontent;
	private String carno;
	private String luxiancontent;
	private String youfei;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLeixing() {
		return leixing;
	}

	public void setLeixing(String leixing) {
		this.leixing = leixing;
	}

	public String getZaizhong() {
		return zaizhong;
	}

	public void setZaizhong(String zaizhong) {
		this.zaizhong = zaizhong;
	}

	public String getXiulicontent() {
		return xiulicontent;
	}

	public void setXiulicontent(String xiulicontent) {
		this.xiulicontent = xiulicontent;
	}

	public String getJiayou() {
		return jiayou;
	}

	public void setJiayou(String jiayou) {
		this.jiayou = jiayou;
	}

	public String getBeizhucontent() {
		return beizhucontent;
	}

	public void setBeizhucontent(String beizhucontent) {
		this.beizhucontent = beizhucontent;
	}

	public String getJiashiyuancontent() {
		return jiashiyuancontent;
	}

	public void setJiashiyuancontent(String jiashiyuancontent) {
		this.jiashiyuancontent = jiashiyuancontent;
	}

	public String getCarno() {
		return carno;
	}

	public void setCarno(String carno) {
		this.carno = carno;
	}

	public String getLuxiancontent() {
		return luxiancontent;
	}

	public void setLuxiancontent(String luxiancontent) {
		this.luxiancontent = luxiancontent;
	}

	public String getYoufei() {
		return youfei;
	}

	public void setYoufei(String youfei) {
		this.youfei = youfei;
	}

	public String toString() {
		return "Cars [id=" + id + ", name=" + name + ", leixing=" + leixing
				+ ", zaizhong=" + zaizhong + ", xiulicontent=" + xiulicontent
				+ ", jiayou=" + jiayou + ", beizhucontent=" + beizhucontent
				+ ", jiashiyuancontent=" + jiashiyuancontent + ", carno="
				+ carno + ", luxiancontent=" + luxiancontent + ", youfei="
				+ youfei + "]";
	}
}